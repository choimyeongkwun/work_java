package ch02;

public class CastingEx1 {
	public static void main(String [] args) {
		double d = 85.4;
		int score = (int)d;
		float c = 85;
		int c1 = (int)0.1f;
		
		System.out.println(c);
		System.out.println(c1);
	    

		
		System.out.println("score=" +score);
		System.out.println("d="+d);
}
}